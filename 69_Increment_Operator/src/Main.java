public class Main {
    public static void main(String[] args) {

        int x=1;
        ++x;
        System.out.println("x="+ x);

        int y=1;
        y++;
        System.out.println("y="+ y);

        int a=1;
        int b=a++;
        System.out.println("a="+ a);
        System.out.println("b="+ b);

        int c=1;
        int d=++c;
        System.out.println("c="+ c);
        System.out.println("d="+ d);
    }
}
